import numpy as np
#rand()
var = np.random.rand(4)
print(var)
print()
#rand() in multidimension
var1 = np.random.rand(2,5)
print(var1)
print()


#randn()
var2 = np.random.randn(5)
print(var2)
print()

#ranf()
var3 = np.random.ranf(4)
print(var3)
print()


#randint(min,max,total)

var4 = np.random.randint(1,20,5)
